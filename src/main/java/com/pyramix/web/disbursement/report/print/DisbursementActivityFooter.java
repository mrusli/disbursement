package com.pyramix.web.disbursement.report.print;

public class DisbursementActivityFooter {

	private String beginningBalance;
	
	private String totalDeposit;
	
	private String totalExpenditure;
	
	private String endingBalance;

	public String getBeginningBalance() {
		return beginningBalance;
	}

	public void setBeginningBalance(String beginningBalance) {
		this.beginningBalance = beginningBalance;
	}

	public String getTotalDeposit() {
		return totalDeposit;
	}

	public void setTotalDeposit(String totalDeposit) {
		this.totalDeposit = totalDeposit;
	}

	public String getTotalExpenditure() {
		return totalExpenditure;
	}

	public void setTotalExpenditure(String totalExpenditure) {
		this.totalExpenditure = totalExpenditure;
	}

	public String getEndingBalance() {
		return endingBalance;
	}

	public void setEndingBalance(String endingBalance) {
		this.endingBalance = endingBalance;
	}
	
}
