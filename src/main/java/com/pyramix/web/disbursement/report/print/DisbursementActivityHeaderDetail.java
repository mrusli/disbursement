package com.pyramix.web.disbursement.report.print;

public class DisbursementActivityHeaderDetail {

	private String theSumOf;
	
	private String disbursementDate;
	
	private String description;
	
	private String disbursementType;
	
	private String disbursementSerialNo;
	
	private String symbolCR;

	public String getTheSumOf() {
		return theSumOf;
	}

	public void setTheSumOf(String theSumOf) {
		this.theSumOf = theSumOf;
	}

	public String getDisbursementDate() {
		return disbursementDate;
	}

	public void setDisbursementDate(String disbursementDate) {
		this.disbursementDate = disbursementDate;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getDisbursementType() {
		return disbursementType;
	}

	public void setDisbursementType(String disbursementType) {
		this.disbursementType = disbursementType;
	}

	public String getDisbursementSerialNo() {
		return disbursementSerialNo;
	}

	public void setDisbursementSerialNo(String disbursementSerialNo) {
		this.disbursementSerialNo = disbursementSerialNo;
	}

	public String getSymbolCR() {
		return symbolCR;
	}

	public void setSymbolCR(String symbolCR) {
		this.symbolCR = symbolCR;
	}
	
}
